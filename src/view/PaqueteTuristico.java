    private String[] listarPaquetesPorLocalizacion(Localizacion localizacion) {

        String[] paquetes = (String[]) file.cargarPaquetesTuristicos(localizacion).toArray();

        DefaultListModel lm = new DefaultListModel();
        for (int i = 0; i < paquetes.length; i++) {
            lm.add(i, paquetes[i]);
        }
        return paquetes;
    }

